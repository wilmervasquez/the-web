function hzToNotaMusical(hz){
  let i = 0;
  if (hz<notas[0][1] || hz > notas[notas.length-1][1]) return null;
  for(let [key,frecuency] of notas){
    let siguienteNota = notas[i+1]
    if (!siguienteNota) break
    let siguienteFrecuency = siguienteNota[1]
    let sigKey = notas[i+1][0]
    if ((frecuency<= hz)&&(hz<=siguienteFrecuency)) {
      let [d1,d2] = [hz-frecuency, siguienteFrecuency - hz]
      return d1 >= d2 ? sigKey : key
    }
    i++
  }
}